#ifndef _MOUSE_H_
#define _MOUSE_H_

#include <Windows.h>
#include "Input.h"


//-----------------------------------------------------------------------------
// マクロ定義
//-----------------------------------------------------------------------------
#define BUTTON_MASK (0x80)


//-----------------------------------------------------------------------------
// 列挙型定義
//-----------------------------------------------------------------------------
enum {
	BUTTON_LEFT = (0),	// 左クリック
	BUTTON_RIGHT,		// 右クリック
	BUTTON_SCROLL,		// スクロールクリック
	BUTTON_NONE,		// 該当キーなし
	BUTTON_MAX
};


//-----------------------------------------------------------------------------
// クラス定義
//-----------------------------------------------------------------------------
class Mouse : public Input {
public:
	//=========================================================================
	// コンストラクタとデストラクタ
	//=========================================================================
	Mouse();
	~Mouse();

	//=========================================================================
	// 初期化処理
	//=========================================================================
	HRESULT Init(HINSTANCE instance, HWND wnd);
	//=========================================================================
	// 更新処理
	//=========================================================================
	void Update();

	//=========================================================================
	// 入力情報の取得
	//=========================================================================
	DIMOUSESTATE GetPressMouse();
	DIMOUSESTATE GetTriggerMouse();

private:
	DIMOUSESTATE pressMouse_;		// マウスの入力状態
	DIMOUSESTATE triggerMouse_;		// マウスのトリガー状態
};


#endif


