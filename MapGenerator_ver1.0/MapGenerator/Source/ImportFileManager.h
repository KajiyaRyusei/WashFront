#ifndef _IMPORT_FILE_MANAGER_H_
#define _IMPORT_FILE_MANAGER_H_

#include "Main.h"


//-----------------------------------------------------------------------------
// STL使用宣言
//-----------------------------------------------------------------------------
using namespace std;
#include <map>
#include <string>


//-----------------------------------------------------------------------------
// クラス定義
//-----------------------------------------------------------------------------
class ImportFileManager {
public:
	//=========================================================================
	// コンストラクタとデストラクタ
	//=========================================================================
	ImportFileManager();
	virtual ~ImportFileManager();

	void Init() {};

	//=========================================================================
	// ファイルのインポート
	//=========================================================================
	int ImportFile(std::string filePath);

	//=========================================================================
	// ファイルをすべてインポート
	//=========================================================================
	void AllImportFile();

	//=========================================================================
	// ファイルパスからファイル名のみ取得
	//=========================================================================
	const char *GetFileName(std::string filePath);
	//=========================================================================
	// ファイル名からファイルのフルパスを取得
	//=========================================================================
	const char *GetFilePath(std::string fileName);

	//=========================================================================
	// インポート情報を外部読み込み
	//=========================================================================
	void ReadImportData(FILE *inputFile);
	//=========================================================================
	// インポート情報を外部出力
	//=========================================================================
	void SaveImportData(FILE *outputFile);

	//=========================================================================
	// ファイルのID取得
	//=========================================================================
	int GetFilePathID(char *filePath);
	//=========================================================================
	// IDからファイルパス取得
	//=========================================================================
	const char *GetFilePath(int ID);


private:
	// ファイルパスとファイル名の管理マップ
	typedef std::map <std::string, std::string> file;
	file	fileNameMap_;
	file	filePathMap_;

};


#endif


