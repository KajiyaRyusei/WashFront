#ifndef _ROUTE_MANAGER_H_
#define _ROUTE_MANAGER_H_

#include "Main.h"



//-----------------------------------------------------------------------------
// STL使用宣言
//-----------------------------------------------------------------------------
using namespace std;
#include <vector>


//-----------------------------------------------------------------------------
// 構造体宣言
//-----------------------------------------------------------------------------
struct RoutePoint{
	D3DXVECTOR3 position;
};


//-----------------------------------------------------------------------------
// クラス定義
//-----------------------------------------------------------------------------
class RouteManager {
public:
	//=========================================================================
	// コンストラクタとデストラクタ
	//=========================================================================
	RouteManager();
	virtual ~RouteManager();

	//=========================================================================
	// 初期化処理
	//=========================================================================
	HRESULT Init();
	//=========================================================================
	// 更新処理
	//=========================================================================
	void Update();
	//=========================================================================
	// 描画処理
	//=========================================================================
	void Draw();

	//=========================================================================
	// データの出力
	//=========================================================================
	void OutputData(FILE *outputFile);
	//=========================================================================
	// データの入力
	//=========================================================================
	void InputData(FILE *inputFile);


	//=========================================================================
	// 点の削除
	//=========================================================================
	void AllDeletePoint();
	//=========================================================================
	// 点の生成
	//=========================================================================
	void CreatePoint();
	//=========================================================================
	// 点の挿入
	//=========================================================================
	void InsertPoint();
	//=========================================================================
	// 点の削除
	//=========================================================================
	void DeletePoint();


	//=========================================================================
	// 座標の設定
	//=========================================================================
	void SetPositionX(float positionX);
	void SetPositionY(float positionY);
	void SetPositionZ(float positionZ);

	//=========================================================================
	// 選択中の点を設定
	//=========================================================================
	void SetPointListCursor(int cursor) { pointListCursor_ = cursor; }


	//=========================================================================
	// 座標の取得
	//=========================================================================
	D3DXVECTOR3 GetPosition();

private:

	std::vector <RoutePoint*>	pointList_;			// ポイントのリスト
	int							pointListCursor_;	// カーソル
};


#endif


