#ifndef _DIRT_H_
#define _DIRT_H_

#include "Main.h"
#include "Scene.h"


//-----------------------------------------------------------------------------
// クラス定義
//-----------------------------------------------------------------------------
class Dirt {
public:
	//=========================================================================
	// コンストラクタとデストラクタ
	//=========================================================================
	Dirt();
	virtual ~Dirt();

	//=========================================================================
	// 初期化処理
	//=========================================================================
	HRESULT Init();
	//=========================================================================
	// 更新処理
	//=========================================================================
	void Update();
	//=========================================================================
	// 描画処理
	//=========================================================================
	void Draw(bool flag);



	//=========================================================================
	// 座標の取得
	//=========================================================================
	D3DXVECTOR3 GetPosition() { return position_; };
	//=========================================================================
	// 半径の取得
	//=========================================================================
	float GetRadius() { return radius_; };

	int GetLevel() { return level_; }


	//=========================================================================
	// X座標の設定
	//=========================================================================
	void SetPositionX(float x) { position_.x = x; };
	//=========================================================================
	// Y座標の設定
	//=========================================================================
	void SetPositionY(float y) { position_.y = y; };
	//=========================================================================
	// Z座標の設定
	//=========================================================================
	void SetPositionZ(float z) { position_.z = z; };
	//=========================================================================
	// 半径の設定
	//=========================================================================
	void SetPosition(D3DXVECTOR3 position) { position_ = position; };

	//=========================================================================
	// 半径の設定
	//=========================================================================
	void SetRadius(float radius) { radius_ = radius; };

	void SetLevel(int level) { level_ = level; }



private:
	D3DXVECTOR3	position_;		// 座標
	float		radius_;		// 半径
	int			level_;
	D3DXMATRIX	worldMatrix_;	// ワールドマトリクス

	static VERTEX_3D vertexBuffer_[9];
};


#endif


