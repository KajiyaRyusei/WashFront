#ifndef _SCENE2D_H_
#define _SCENE2D_H_

#include "Main.h"
#include "Scene.h"


//-----------------------------------------------------------------------------
// クラス定義
//-----------------------------------------------------------------------------
class Scene2D : public Scene {
public:
	//=========================================================================
	// コンストラクタとデストラクタ
	//=========================================================================
	Scene2D(int priority = 0);
	virtual ~Scene2D();

	//=========================================================================
	// 初期化処理
	//=========================================================================
	HRESULT Init() { return S_OK; };
	HRESULT Init(D3DXVECTOR3 pos, float width, float height, D3DXCOLOR color);
	//=========================================================================
	// 更新処理
	//=========================================================================
	void Update();
	//=========================================================================
	// 描画処理
	//=========================================================================
	void Draw();
	//=========================================================================
	// 生成処理
	//=========================================================================
	static Scene2D *Create(D3DXVECTOR3 pos, float width, float height, D3DXCOLOR color);

	//=========================================================================
	// 色の設定
	//=========================================================================
	void SetColor(D3DXCOLOR color);
	//=========================================================================
	// 点滅の設定
	//=========================================================================
	void SetFlash(BOOL flag, float speed) { flashFlag_ = flag; flashSpeed_ = speed; };
	//=========================================================================
	// 目標幅の設定
	//=========================================================================
	void SetDestWidth(float destWidth) { destWidth_ = destWidth; };
	//=========================================================================
	// 目標高さの設定
	//=========================================================================
	void SetDestHeight(float destHeight) { destHeight_ = destHeight; };
	//=========================================================================
	// 幅の設定
	//=========================================================================
	void SetWidth(float width);
	//=========================================================================
	// 高さの設定
	//=========================================================================
	void SetHeight(float height);
	//=========================================================================
	// 目標座標の設定
	//=========================================================================
	void SetDestPosition(D3DXVECTOR3 destPosition) { destPosition_ = destPosition; };



protected:
	LPDIRECT3DVERTEXBUFFER9	vertexBuffer_;  // 頂点情報格納ワーク
	D3DXVECTOR3				rotation_;  // 向き
	float					width_;  // 幅
	float					height_;  // 高さ
	float					destWidth_;
	float					destHeight_;
	D3DXVECTOR3				destPosition_;
	BOOL					flashFlag_;  // 点滅フラグ
	float					flashSpeed_;  // 点滅速度
};


#endif


