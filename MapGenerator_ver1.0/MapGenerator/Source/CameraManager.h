#ifndef _CAMERA_MANAGER_H_
#define _CAMERA_MANAGER_H_

#include <Windows.h>
#include "Main.h"
#include "Camera.h"
#include "EditorCamera.h"


//-----------------------------------------------------------------------------
// 前方宣言
//-----------------------------------------------------------------------------
class Camera;


//-----------------------------------------------------------------------------
// クラス定義
//-----------------------------------------------------------------------------
class CameraManager {
public:
	//=========================================================================
	// コンストラクタとデストラクタ
	//=========================================================================
	CameraManager();
	virtual ~CameraManager();

	//=========================================================================
	// 初期化処理
	//=========================================================================
	HRESULT Init();
	//=========================================================================
	// 更新処理
	//=========================================================================
	void Update();
	//=========================================================================
	// カメラの設定処理
	//=========================================================================
	void SetCamera();

	//=========================================================================
	// インスタンスの取得
	//=========================================================================
	Camera *GetCamera() { return camera_; };

	//=========================================================================
	// カメラの解放
	//=========================================================================
	void ReleaseCamera() {
		if (camera_) {
			delete camera_;
			camera_ = nullptr;
		}
	};
	//=========================================================================
	// ゲームカメラの生成
	//=========================================================================
	void CreateEditorCamera() {
		if (!camera_) {
			camera_ = new EditorCamera();
			camera_->Init();
		}
	};


private:
	Camera *camera_;	// カメラ
};


#endif


