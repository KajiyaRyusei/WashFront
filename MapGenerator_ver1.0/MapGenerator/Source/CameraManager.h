#ifndef _CAMERA_MANAGER_H_
#define _CAMERA_MANAGER_H_

#include <Windows.h>
#include "Main.h"
#include "Camera.h"
#include "EditorCamera.h"
#include "PreviewCamera.h"


//-----------------------------------------------------------------------------
// 前方宣言
//-----------------------------------------------------------------------------
class Camera;


//-----------------------------------------------------------------------------
// クラス定義
//-----------------------------------------------------------------------------
class CameraManager {
public:
	//=========================================================================
	// コンストラクタとデストラクタ
	//=========================================================================
	CameraManager();
	virtual ~CameraManager();

	//=========================================================================
	// 初期化処理
	//=========================================================================
	HRESULT Init();
	//=========================================================================
	// 更新処理
	//=========================================================================
	void Update();
	//=========================================================================
	// カメラの設定処理
	//=========================================================================
	void SetCamera();

	//=========================================================================
	// インスタンスの取得
	//=========================================================================
	Camera *GetCamera() { return camera_; };

	//=========================================================================
	// ゲームカメラの生成
	//=========================================================================
	void ChangeEditorCamera() {
		camera_ = edit_;
		//camera_->Init();
	};
	void ChangePreviewCamera(int index) {
		camera_ = preview_;
		static_cast<PreviewCamera*>(camera_)->Init(index);
	};


private:
	Camera *camera_;
	Camera *edit_;
	Camera *preview_;
};


#endif


